public class Variabel {
    public static void main(String[] args) {
        // Deklarasikan variabel di sini, sesuai dengan tipe datanya
        int x = 123;
        float y = 243.584F;
        String z = "Hello";

        // Cetak variabel di sini
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
    }
}
